public class Modulus {
	public static void main(String[] args) {
		int a = 100;
		int b = 2;
		int z = a-b * (a/b);
		if (z == 0) {
			System.out.println("The number " + a + " is Even");
			
		}
		else {
			System.out.println("the number " + a + " is odd");
		}
		
	}
}

	
	


