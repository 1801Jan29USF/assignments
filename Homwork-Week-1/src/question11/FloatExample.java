package question11;

public class FloatExample {
	
	//two float numbers 
	private float x=500000f;
	private float y = 104564f;
	/**
	 * @return the x
	 */
	
	//methods to be used by object to access float from another package
	public float getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(float x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public float getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(float y) {
		this.y = y;
	}
	
	
	
	

}
